void BSADC_IA_SetTrim(uint8_t posTrim, uint8_t negTrim)
{
	SDADC->BSINISTRAMP = (SDADC->BSINISTRAMP &  ~SDADC_BSINISTRAMP_OFFSETPEN_Msk)|(  (posTrim != 0)<<SDADC_BSINISTRAMP_OFFSETPEN_Pos);
	SDADC->BSINISTRAMP = (SDADC->BSINISTRAMP &  ~SDADC_BSINISTRAMP_OFFSETP_Msk)|(  posTrim<<SDADC_BSINISTRAMP_OFFSETP_Pos);
	//SDADC->BSINISTRAMP.BSIA_OFFSETP = posTrim;

	//SDADC->BSINISTRAMP.BSIA_OFFSETNEN = (negTrim != 0);
	//SDADC->BSINISTRAMP.BSIA_OFFSETN =  negTrim;
	SDADC->BSINISTRAMP = (SDADC->BSINISTRAMP &  ~SDADC_BSINISTRAMP_OFFSETNEN_Msk)|(  (negTrim != 0)<<SDADC_BSINISTRAMP_OFFSETNEN_Pos);
	SDADC->BSINISTRAMP = (SDADC->BSINISTRAMP &  ~SDADC_BSINISTRAMP_OFFSETN_Msk)|(  negTrim<<SDADC_BSINISTRAMP_OFFSETN_Pos);
	
}

